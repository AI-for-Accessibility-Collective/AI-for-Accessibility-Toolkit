const isWatch = process.argv.includes('--watch');

// ---------------------------------------------------------------------------
// Browser-harness skills manifest
// ---------------------------------------------------------------------------
// browser-harness-orig/ holds bundled markdown knowledge files (interaction
// patterns + per-site playbooks). Service-worker fetch can resolve them via
// chrome.runtime.getURL but cannot list directories, so we pre-bake an index
// at build time. Change BH_ORIG_PATH in one place if the directory is renamed
// or the python files are stripped out — the runtime path constant in
// extension/browser-harness/skills.js mirrors this.
const BH_ORIG_PATH = 'extension/browser-harness/browser-harness-orig';
const BH_INTERACTION_SUBPATH = 'interaction-skills';
const BH_DOMAIN_SUBPATH = 'agent-workspace/domain-skills';
const BH_MANIFEST_OUT = 'extension/browser-harness/skills-manifest.json';

function listMd(dir) {
  if (!fs.existsSync(dir)) return [];
  return fs.readdirSync(dir)
    .filter((f) => f.endsWith('.md'))
    .map((f) => f.replace(/\.md$/, ''))
    .sort();
}

function buildSkillsManifest() {
  const interactionDir = path.resolve(__dirname, BH_ORIG_PATH, BH_INTERACTION_SUBPATH);
  const domainDir = path.resolve(__dirname, BH_ORIG_PATH, BH_DOMAIN_SUBPATH);
  const interaction = listMd(interactionDir);
  // Manifest entry: { dir: <on-disk name>, files: [...] }. Lookup key is the
  // lowercased dir name so a normalized hostname like "boss-zhipin" still
  // resolves the upstream "BOSS-zhipin" folder without renaming the tree.
  const domain = {};
  if (fs.existsSync(domainDir)) {
    for (const dirName of fs.readdirSync(domainDir).sort()) {
      const hdir = path.join(domainDir, dirName);
      try {
        if (!fs.statSync(hdir).isDirectory()) continue;
      } catch { continue; }
      const files = listMd(hdir);
      if (files.length) {
        const key = dirName.toLowerCase();
        domain[key] = { dir: dirName, files };
      }
    }
  }
  fs.mkdirSync(path.dirname(path.resolve(__dirname, BH_MANIFEST_OUT)), { recursive: true });
  fs.writeFileSync(
    path.resolve(__dirname, BH_MANIFEST_OUT),
    JSON.stringify({ interaction, domain }, null, 2)
  );
  console.log(
    `skills-manifest.json: ${interaction.length} interaction skills, ${Object.keys(domain).length} domain hosts`
  );
}

// ---------------------------------------------------------------------------
// Built-in tools registry (global Tools db)
// ---------------------------------------------------------------------------
// skills/registry.js is the canonical ESM source (node-side consumers import
// it directly). Extension pages and the background SW can't load ES modules,
// so we bake the registry into a classic script that assigns
// globalThis.AA_TOOLS — same pattern as skills-manifest.json above.
const TOOLS_REGISTRY_OUT = 'extension/lib/tools-registry.js';

async function buildToolsRegistry() {
  const { skillRegistry, settingsMeta } = await import('./skills/registry.js');
  const out = `// GENERATED by build.js from skills/registry.js — do not edit by hand.
// Canonical source: personalized-extension/skills/registry.js
// Loaded by background.js (importScripts) and extension pages (script tag).
globalThis.AA_TOOLS = {
  version: 1,
  list: ${JSON.stringify(skillRegistry, null, 2)},
  settingsMeta: ${JSON.stringify(settingsMeta, null, 2)},
  // One prompt-ready line per setting: "key (type...): description".
  settingsVocabularyLines() {
    return Object.entries(this.settingsMeta).map(([k, m]) => {
      let t = m.type;
      if (m.type === 'number' && m.range) t = \`number \${m.range[0]}-\${m.range[1]}\`;
      if (m.type === 'enum' && m.options) t = 'string: ' + m.options.map(o => '"' + o + '"').join(', ');
      return \`- \${k} (\${t}): \${m.description}\`;
    });
  },
  byId(id) { return this.list.find(s => s.id === id) || null; },
  byArea(area) { return this.list.filter(s => s.supportAreas.includes(area)); },
  // chrome.storage.sync settings activated by enabling each of \`ids\`.
  settingsFor(ids) {
    const merged = {};
    for (const id of ids || []) {
      const t = this.byId(id);
      if (t && t.settings) Object.assign(merged, t.settings);
    }
    return merged;
  },
  forPrompt() {
    return this.list.map(s => ({
      id: s.id, name: s.name, description: s.description,
      supportAreas: s.supportAreas, siteRelevance: s.siteRelevance,
    }));
  },
};
`;
  const outPath = path.resolve(__dirname, TOOLS_REGISTRY_OUT);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, out);
  console.log(`tools-registry.js: ${skillRegistry.length} built-in tools`);
}

