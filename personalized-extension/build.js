// ---------------------------------------------------------------------------
// Toolkit core (portable personal-memory engine) → classic-script globals
// ---------------------------------------------------------------------------
// The reference implementation lives in /toolkit (ES modules, platform-
// agnostic, port-injected). The service worker (importScripts) and the popup
// (<script>) load classic scripts that assign globalThis.AA_TAXONOMY /
// Datastore / Librarian, so each chrome adapter entry is bundled to an IIFE --
// same pattern as harness.js / agent.js / tools-registry.js. Edit the source
// under /toolkit; lib/{taxonomy,datastore,librarian}.js are generated.
const TOOLKIT_CHROME = path.resolve(__dirname, '..', 'toolkit', 'adapters', 'chrome');
const TOOLKIT_BANNER = '// GENERATED by build.js from /toolkit (ES module source). Do not edit by hand.';

function toolkitConfig(entry, outName) {
  return {
    entryPoints: [path.resolve(TOOLKIT_CHROME, entry)],
    bundle: true,
    outfile: path.resolve(__dirname, 'extension/lib', outName),
    format: 'iife',
    target: 'chrome110',
    sourcemap: false,
    logLevel: 'info',
    banner: { js: TOOLKIT_BANNER },
  };
}

const taxonomyConfig = toolkitConfig('taxonomy.entry.js', 'taxonomy.js');
const datastoreConfig = toolkitConfig('datastore.entry.js', 'datastore.js');
const librarianConfig = toolkitConfig('librarian.entry.js', 'librarian.js');
const webSurfaceConfig = toolkitConfig('web-surface.entry.js', 'web-surface.js');

// ---------------------------------------------------------------------------
// aria-query build-time tables
// ---------------------------------------------------------------------------
// Generates utils/aria-tables.gen.js (valid ARIA roles minus abstract + valid
// aria-* attribute names) from the aria-query package so wcag-fixes.js and
// constants.js never hand-roll these lists.
const ARIA_TABLES_OUT = 'utils/aria-tables.gen.js';

function buildAriaTables() {
  const aq = require('aria-query');
  // Roles: iterate the Map to find non-abstract ones.
  const concreteRoles = [];
  aq.roles.forEach((def, name) => {
    if (!def.abstract) concreteRoles.push(name);
  });
  concreteRoles.sort();

  // ARIA attribute names.
  const ariaProps = [];
  aq.aria.forEach((_def, name) => ariaProps.push(name));
  ariaProps.sort();

  const out =
    '// GENERATED by build.js from aria-query — do not edit by hand.\n' +
    '// Source: personalized-extension/build.js buildAriaTables()\n' +
    '// Re-generate: npm run build\n\n' +
    `export const VALID_ARIA_ROLES = new Set(${JSON.stringify(concreteRoles, null, 2)});\n\n` +
    `export const VALID_ARIA_ATTRS = new Set(${JSON.stringify(ariaProps, null, 2)});\n`;

  const outPath = path.resolve(__dirname, ARIA_TABLES_OUT);
  fs.mkdirSync(path.dirname(outPath), { recursive: true });
  fs.writeFileSync(outPath, out);
  console.log(`aria-tables.gen.js: ${concreteRoles.length} concrete roles, ${ariaProps.length} aria attrs`);
}

// ---------------------------------------------------------------------------
// axe-core asset copy
// ---------------------------------------------------------------------------
// Copies node_modules/axe-core/axe.min.js into extension/lib/ so the SW can
// inject it with chrome.scripting.executeScript({ files: ['lib/axe.min.js'] }).
const AXE_SRC = 'node_modules/axe-core/axe.min.js';
const AXE_DST = 'extension/lib/axe.min.js';

function copyAxe() {
  const src = path.resolve(__dirname, AXE_SRC);
  const dst = path.resolve(__dirname, AXE_DST);
  fs.mkdirSync(path.dirname(dst), { recursive: true });
  fs.copyFileSync(src, dst);
  const size = Math.round(fs.statSync(dst).size / 1024);
  console.log(`axe.min.js copied (${size} kB) → extension/lib/axe.min.js`);
}

async function build() {
  buildSkillsManifest();
  buildAriaTables();
  copyAxe();
  await buildToolsRegistry();
  if (isWatch) {
    const ctxs = await Promise.all([
      esbuild.context(contentConfig),
      esbuild.context(harnessConfig),
      esbuild.context(agentConfig),
      esbuild.context(offscreenConfig),
      esbuild.context(sidepanelConfig),
      esbuild.context(taxonomyConfig),
      esbuild.context(datastoreConfig),
      esbuild.context(librarianConfig),
      esbuild.context(webSurfaceConfig),
    ]);
    await Promise.all(ctxs.map((c) => c.watch()));
    console.log('Watching for changes...');
  } else {
    await Promise.all([
      esbuild.build(contentConfig),
      esbuild.build(harnessConfig),
      esbuild.build(agentConfig),
      esbuild.build(offscreenConfig),
      esbuild.build(sidepanelConfig),
      esbuild.build(taxonomyConfig),
      esbuild.build(datastoreConfig),
      esbuild.build(librarianConfig),
      esbuild.build(webSurfaceConfig),
    ]);
    console.log('Build complete.');
  }
}

build().catch((e) => {
  console.error(e);
  process.exit(1);
});
